    private void jSucursalesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jSucursalesMouseClicked
        try{
            fila = jSucursales.getSelectedRow();
            identidad1=jSucursales.getValueAt(fila,0).toString();
            ResultSet rs;
            Connection con = cone.establecerConexion();
            
            CallableStatement cmd = con.prepareCall("{CALL sp_mostrarsucursalespecifico(?)}");
            cmd.setString(1, identidad1);
            rs = cmd.executeQuery();
            
            while(rs.next()){
                codigo = Integer.valueOf(rs.getInt("IdSucursal"));
                txtSucursal.setText(rs.getString("Nombre"));
                txtDireccion.setText(rs.getString("Direccion"));
              }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_jSucursalesMouseClicked

